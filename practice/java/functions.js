function math() {
    console.log(1 + 1);
    console.log(2 - 1);
    console.log(4 / 2);
    console.log(5 * 2);
}

math();

function math2(firstnumber) {
    console.log(firstnumber + 1);
    console.log(firstnumber - 1);
    console.log(firstnumber / 2);
    console.log(firstnumber * 2);
}

math2(10)
math2(100);
math2(1000)
